      <p class="text-moving" style="font-weight:bold">CREATION DE L'ESMV</p>
        <p class="text-moving">
        
        L'Ecole des Sciences et de Médecine vétérinaire (ESMV) est un établissement créé en 1993 par décret présidentiel n° 93/028 du 19 janvier 1993, portant sur l'organisation administrative et académique de l'Université de Ngaoundéré.
        
        </p>
        <p class="text-moving">
        Elle a été ouverte par arrêté n° 07/0176/MINESUP du 30 octobre 2007, portant ouverture de l'ESMV de l'Université de Ngaoundéré avec pour objectifs la formation des cadres de haut niveau en santé et productions animales, l'appui au développement, le recyclage et le perfectionnement des cadres en santé et productions animales.
        </p>
        <p class="text-moving">
        L'admission se fait sur concours (ouvert aux candidats camerounais et étrangers titulaires du Baccalauréat C ou D, du CGE A/L en 2 matières scientifiques y compris la biologie ou tout autre diplôme équivalent), et sur études de dossiers pour les candidats camerounais, des pays de la zone CEMAC ou étrangers dans la limite des places disponibles.
        </p>
        <p class="text-moving">
        Le concours, sur épreuves, est ouvert pour le recrutement de quarante (40) élèves en première année du cycle des Ingénieurs des Travaux en Sciences Vétérinaires de l’Ecole des Sciences et de Médecine Vétérinaire (ESMV) de l'Université de Ngaoundéré. Ce concours a dans les centres de Buea, Dschang, Maroua, Ngaoundéré et Yaoundé.
Le concours est ouvert aux Camerounais des deux sexes, âgés de vingt six (26) au plus et titulaires d'un Baccalauréat série C ou D, ou d'un General Certificate of Education Advanced Level (GCE "A" Level) au moins dans trois matières scientifiques dont la biologie, ou d'un diplôme reconnu équivalent par le Ministère de l’Enseignement Supérieur.

        </p>

        <p class="text-moving">
        Les candidats ressortissants des pays membres de la Communauté
Economique et Monétaire de l'Afrique Centrale (CEMAC) ayant ratifié les textes relatifs au traitement national des étudiants et justifiant des qualifications requises sont admis à composer dans les mêmes conditions que leurs homologues camerounais.

        </p>
        <p class="text-moving">
        	Les candidats nationaux ou étrangers qui ne souhaitent pas participer au concours d'entrée peuvent être admis sur étude de dossier dans la limite des places disponibles (avec un maximum de dix places), s'ils sont bénéficiaires d'une bourse de leur pays ou d'une Organisation Internationale qui s'engage à payer les frais de
scolarité dont le montant annuel est fixé à un million (1.000.000) de FCFA.

        </p>
        <p class="text-moving">
        Chaque candidat doit faire parvenir au Service de la Scolarité de l’ESMV,  un dossier complet comprenant les pièces suivantes :
    
        </p>
        <p class="text-moving">
        •	une fiche individuelle dûment remplie à retirer à l'ESMV de l’Université de Ngaoundéré, dans les six autres Universités d’Etat, dans les Délégations Régionales des Enseignements secondaires, dans les Antennes de l'Université de Dschang à Yaoundé, Maroua, Ebolowa, Bambili et Belabo et celles de l’Université de Ngaoundéré à Yaoundé et Bertoua. Cette fiche est également disponible sur le site web du Ministère de l’Enseignement Supérieur : <a href="">www.minsup.gov.com</a>, et celui de l’Université de Ngaoundéré : <a href="" >www.univ-ndere.com</a>
        </p>
        <p class="text-moving">
        •	une photocopie certifiée conforme du Baccalauréat, du GCE "A" Level ou de tout autre diplôme (séries scientifiques) reconnu équivalent, ou une photocopie certifiée conforme de l’attestation de réussite à l'un de ces diplômes ;
        </p>
        <p class="text-moving">
        •	des photocopies certifiées conformes des relevés de notes du Baccalauréat et du Probatoire, ou une photocopie certifiée conforme de réussite au GCE "A" Level et GCE "O" Level ;
        </p>
        <p class="text-moving">
        •	un certificat médical délivré par un médecin de la Santé Publique attestant que le candidat est apte aux travaux de terrain et à la pratique du sport ;
        </p>
        <p class="text-moving">
        •	un reçu de paiement de la somme de quinze mille (15.000) FCFA représentant les frais de concours (frais de versement ou de transfert non compris) soit à verser sur le compte de l’ESMV N° 01207980601/11 domicilié à ECOBANK, soit à payer par Express Union ou par Express Exchanqe au nom de l’ESMV- Université de Ngaoundéré.
        </p>
        <p class="text-moving">
        •	une enveloppe (format A4) timbrée au tarif réglementaire et portant l'adresse complète du candidat ou celle de ses parents.
        </p>
        
