        CONDISTIONS D'ADMISSION A L'ESMV
            
            </p>
            <p class="text-moving">
            Le concours a été ouverte par arrêté n° 07/0176/MINESUP du 30 octobre 2007, portant ouverture de l'ESMV de l'Université de Ngaoundéré avec pour objectifs la formation des cadres de haut niveau en santé et productions animales, l'appui au développement, le recyclage et le perfectionnement des cadres en santé et productions animales.
            </p>
            <p class="text-moving">
            Les candidats nationaux ou étrangers qui ne souhaitent pas participer au concours d'entrée peuvent être admis sur étude de dossier dans la limite des places disponibles (avec un maximum de dix places), s'ils sont bénéficiaires d'une bourse de leur pays ou d'une Organisation Internationale qui s'engage à payer les frais de
scolarité dont le montant annuel est fixé à un million (1.000.000) de FCFA.

            </p>
            <p class="text-moving">
            Chaque candidat doit faire parvenir au Service de la Scolarité de l’ESMV,  un dossier complet comprenant les pièces suivantes :
            </p>

            <p class="text-moving">
            •   Une fiche individuelle dûment remplie à retirer à l'ESMV de l’Université de Ngaoundéré, dans les six autres Universités d’Etat, dans les Délégations Régionales des Enseignements secondaires, dans les Antennes de l'Université de Dschang à Yaoundé, Maroua, Ebolowa, Bambili et Belabo et celles de l’Université de Ngaoundéré à Yaoundé et Bertoua. Cette fiche est également disponible sur le site web du Ministère de l’Enseignement Supérieur : http : //www.minsup.gov.com, et celui de l’Université de Ngaoundéré : www.univ-ndere.com            </p>
            <p class="text-moving">
            •	Une photocopie certifiée conforme du Baccalauréat, du GCE "A" Level ou de tout autre diplôme (séries scientifiques) reconnu équivalent, ou une photocopie certifiée conforme de l’attestation de réussite à l'un de ces diplômes ;
            </p>
            <p class="text-moving">
            •	Des photocopies certifiées conformes des relevés de notes du Baccalauréat et du Probatoire, ou une photocopie certifiée conforme de réussite au GCE "A" Level et GCE "O" Level ;
            
            </p>
            <p class="text-moving">
            •	Une photocopie certifiée conforme de l'acte de naissance datant de moins de 3 mois ;
            </p>
            <p class="text-moving">
            •	Quatre photos d'identité (4x4) récentes ;
            </p>
            <p class="text-moving">
            •	Un certificat médical délivré par un médecin de la Santé Publique attestant que le candidat est apte aux travaux de terrain et à la pratique du sport ;
            </p>
            <p class="text-moving">
            •	Un reçu de paiement de la somme de quinze mille (15.000) FCFA représentant les frais de concours (frais de versement ou de transfert non compris) soit à verser sur le compte de l’ESMV N° 01207980601/11 domicilié à ECOBANK, soit à payer par Express Union ou par Express Exchanqe au nom de l’ESMV- Université de Ngaoundéré.
            </p>
            <p class="text-moving">
            •	Une enveloppe (format A4) timbrée au tarif réglementaire et portant l'adresse complète du candidat ou celle de ses parents.
            </p>
            <p class="text-moving">
            •	Relevés des notes du Baccalauréat et du Probatoire ;
            </p>
            <p class="text-moving">
            •	Résultats du GCE « A » Level et du GCE « O » Level ;
            </p>
            <p class="text-moving">
            •	Relevés des notes du Baccalauréat et du Probatoire ;
            </p>
            <p class="text-moving">
            •	Nombre d’années écoulées depuis l’obtention du baccalauréat ou du GCE « A » level.
L’évaluation pour l’admission finale des candidats se fera suivant les proportions ci-après : 80% de la note finale pour les épreuves écrites et 20% pour les performances antérieures du candidat

